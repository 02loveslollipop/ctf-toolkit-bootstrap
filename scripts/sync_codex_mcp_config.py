#!/usr/bin/env python3
"""Sync OpenCROW MCP server entries into a Codex config.toml file."""

from __future__ import annotations

import argparse
import re
from pathlib import Path


OPEN_CROW_MCP_SERVERS = [
    "opencrow-crypto-mcp",
    "opencrow-forensics-mcp",
    "opencrow-minecraft-mcp",
    "opencrow-netcat-mcp",
    "opencrow-network-mcp",
    "opencrow-osint-mcp",
    "opencrow-pwn-mcp",
    "opencrow-reversing-mcp",
    "opencrow-ssh-mcp",
    "opencrow-stego-mcp",
    "opencrow-utility-mcp",
    "opencrow-web-mcp",
]
START_MARKER = "# >>> OpenCROW managed MCP servers >>>"
END_MARKER = "# <<< OpenCROW managed MCP servers <<<"
TABLE_RE = re.compile(r"^\[(?P<header>[^\]]+)\]\s*$")


def strip_managed_block(text: str) -> str:
    pattern = re.compile(
        rf"\n?{re.escape(START_MARKER)}\n.*?{re.escape(END_MARKER)}\n?",
        re.DOTALL,
    )
    return pattern.sub("\n", text)


def split_sections(text: str) -> list[tuple[str | None, str]]:
    sections: list[tuple[str | None, str]] = []
    current_lines: list[str] = []
    current_header: str | None = None

    for line in text.splitlines(keepends=True):
        match = TABLE_RE.match(line.strip())
        if match:
            if current_lines:
                sections.append((current_header, "".join(current_lines)))
            current_header = match.group("header")
            current_lines = [line]
            continue
        current_lines.append(line)

    if current_lines:
        sections.append((current_header, "".join(current_lines)))

    return sections


def remove_existing_opencrow_tables(text: str) -> str:
    managed_headers = {f"mcp_servers.{name}" for name in OPEN_CROW_MCP_SERVERS}
    kept_sections: list[str] = []
    for header, section_text in split_sections(text):
        if header in managed_headers:
            continue
        kept_sections.append(section_text)
    return "".join(kept_sections)


def build_managed_block(target_home: Path, startup_timeout_sec: int) -> str:
    lines = [
        START_MARKER,
        "# Generated by scripts/sync_codex_mcp_config.py.",
        "# These entries are managed by OpenCROW and may be replaced on reinstall/update.",
        "",
    ]
    for name in OPEN_CROW_MCP_SERVERS:
        lines.append(f"[mcp_servers.{name}]")
        lines.append(f'command = "{target_home / ".local/bin" / name}"')
        lines.append(f"startup_timeout_sec = {startup_timeout_sec}")
        lines.append("")
    lines.append(END_MARKER)
    return "\n".join(lines) + "\n"


def sync_config(config_path: Path, target_home: Path, startup_timeout_sec: int) -> str:
    existing = config_path.read_text() if config_path.exists() else ""
    cleaned = remove_existing_opencrow_tables(strip_managed_block(existing)).strip()
    managed_block = build_managed_block(target_home, startup_timeout_sec).rstrip()

    if cleaned:
        return f"{cleaned}\n\n{managed_block}\n"
    return f"{managed_block}\n"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--config",
        default="~/.codex/config.toml",
        help="Path to the Codex config.toml file.",
    )
    parser.add_argument(
        "--target-home",
        default=str(Path.home()),
        help="Home directory where the OpenCROW MCP commands are installed.",
    )
    parser.add_argument(
        "--startup-timeout-sec",
        type=int,
        default=20,
        help="startup_timeout_sec value to apply to each managed OpenCROW MCP entry.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print the resulting config instead of writing it.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    config_path = Path(args.config).expanduser()
    target_home = Path(args.target_home).expanduser().resolve()
    output = sync_config(config_path, target_home, args.startup_timeout_sec)

    if args.dry_run:
        print(output, end="")
        return 0

    config_path.parent.mkdir(parents=True, exist_ok=True)
    config_path.write_text(output)
    print(
        f"Synced {len(OPEN_CROW_MCP_SERVERS)} OpenCROW MCP entries into {config_path} "
        f"with startup_timeout_sec={args.startup_timeout_sec}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
